func RenderDockerCompose(cfg *SetupConfig) (string, error) {
	return renderTemplate("templates/docker-compose.yml.tmpl", cfg)
}

func RenderCaddyfile(cfg *SetupConfig) (string, error) {
	return renderTemplate("templates/Caddyfile.tmpl", cfg)
}

// defaultAdminHash is the hardcoded default admin password hash in db/init-postgres.sql.
// RenderInitPostgres replaces it with the setup-generated hash so there is a single
// source of truth for the Postgres schema (db/init-postgres.sql).
const defaultAdminHash = "$2a$10$r0TpEb0RiqoaP2HuhvDzaONZe0jaTpjlqzGY3.xY8ZqrvjZNNyOBC"

func RenderInitPostgres(cfg *SetupConfig) (string, error) {
	sql := strings.Replace(dbsql.PostgresSQL, defaultAdminHash, cfg.AdminPasswordHash, 1)
	// bifract generates a random admin password, so disable forced password change.
	// The default init SQL sets force_password_change=TRUE for the hardcoded admin:bifract credentials.
	sql = strings.Replace(sql, "TRUE,\n    TRUE\n)\nON CONFLICT", "TRUE,\n    FALSE\n)\nON CONFLICT", 1)
	return sql, nil
}

func RenderInitClickHouse(_ *SetupConfig) (string, error) {
	return dbsql.ClickHouseSQL, nil
}

