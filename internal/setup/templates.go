func renderTemplate(name string, data interface{}) (string, error) {
	content, err := TemplateFS.ReadFile(name)
	if err != nil {
		return "", fmt.Errorf("read template %s: %w", name, err)
	}

	tmpl, err := template.New(filepath.Base(name)).Parse(string(content))
	if err != nil {
		return "", fmt.Errorf("parse template %s: %w", name, err)
	}

	var buf bytes.Buffer
	if err := tmpl.Execute(&buf, data); err != nil {
		return "", fmt.Errorf("execute template %s: %w", name, err)
	}
	return buf.String(), nil
}

func CopyEmbeddedFile(name, destPath string) error {
	content, err := TemplateFS.ReadFile(name)
	if err != nil {
		return fmt.Errorf("read embedded %s: %w", name, err)
	}
	if err := os.MkdirAll(filepath.Dir(destPath), 0755); err != nil {
		return err
	}
	return os.WriteFile(destPath, content, 0755)
}

// WriteAllFiles generates and writes all configuration files to the install directory.
func WriteAllFiles(cfg *SetupConfig) error {
	dir := cfg.InstallDir

	compose, err := RenderDockerCompose(cfg)
	if err != nil {
		return fmt.Errorf("render docker-compose: %w", err)
	}
	if err := writeFile(filepath.Join(dir, "docker-compose.yml"), compose); err != nil {
		return err
	}

	envContent := RenderEnvFile(cfg)
	if err := writeSecretFile(filepath.Join(dir, ".env"), envContent); err != nil {
		return err
	}

	caddyfile, err := RenderCaddyfile(cfg)
	if err != nil {
		return fmt.Errorf("render caddyfile: %w", err)
	}
	if err := os.MkdirAll(filepath.Join(dir, "caddy"), 0755); err != nil {
		return err
	}
	if err := writeFile(filepath.Join(dir, "caddy", "Caddyfile"), caddyfile); err != nil {
		return err
	}

	// Generate or copy TLS certificates for self-signed and custom modes.
	certsDir := filepath.Join(dir, "caddy", "certs")
	switch cfg.SSLMode {
	case SSLSelfSigned:
		if err := GenerateSelfSignedCert(cfg.Domain, certsDir); err != nil {
			return fmt.Errorf("generate self-signed cert: %w", err)
		}
	case SSLCustom:
		if cfg.CertPath != "" && cfg.KeyPath != "" {
			if err := copyCertsToDir(cfg.CertPath, cfg.KeyPath, certsDir); err != nil {
				return fmt.Errorf("copy custom certs: %w", err)
			}
		}
	}

	// Generate client CA for mTLS mode.
	if cfg.IPAccess == IPAccessMTLSApp {
		caDir := filepath.Join(dir, "caddy", "client-ca")
		if err := GenerateClientCA(caDir); err != nil {
			return fmt.Errorf("generate client CA: %w", err)
		}
	}

	if err := CopyEmbeddedFile("templates/entrypoint.sh", filepath.Join(dir, "caddy", "entrypoint.sh")); err != nil {
		return err
	}
	if err := CopyEmbeddedFile("templates/ship-logs.sh", filepath.Join(dir, "caddy", "ship-logs.sh")); err != nil {
		return err
	}

	initPG, err := RenderInitPostgres(cfg)
	if err != nil {
		return fmt.Errorf("render init-postgres: %w", err)
	}
	if err := os.MkdirAll(filepath.Join(dir, "db"), 0755); err != nil {
		return err
	}
	if err := writeFile(filepath.Join(dir, "db", "init-postgres.sql"), initPG); err != nil {
		return err
	}

	initCH, err := RenderInitClickHouse(cfg)
	if err != nil {
		return fmt.Errorf("render init-clickhouse: %w", err)
	}
	if err := writeFile(filepath.Join(dir, "db", "init-clickhouse.sql"), initCH); err != nil {
		return err
	}

	if err := CopyEmbeddedFile("templates/litellm-config.yaml", filepath.Join(dir, "litellm-config.yaml")); err != nil {
		return err
	}

	return nil
}

func writeFile(path, content string) error {
	if err := os.MkdirAll(filepath.Dir(path), 0755); err != nil {
		return err
	}
	return os.WriteFile(path, []byte(content), 0644)
}

func writeSecretFile(path, content string) error {
	if err := os.MkdirAll(filepath.Dir(path), 0755); err != nil {
		return err
	}
	return os.WriteFile(path, []byte(content), 0600)
}
